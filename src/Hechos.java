public class Hechos {
    double temperatura;
    double vibracion;
    String ruido;
    String frecuenciaVibracion;
    String presion;
    double amperaje;

    public Hechos(double temperatura, double vibracion, String ruido,
                  String frecuenciaVibracion, String presion, double amperaje) {

        this.temperatura = temperatura;
        this.vibracion = vibracion;
        this.ruido = ruido;
        this.frecuenciaVibracion = frecuenciaVibracion;
        this.presion = presion;
        this.amperaje = amperaje;
    }
}
